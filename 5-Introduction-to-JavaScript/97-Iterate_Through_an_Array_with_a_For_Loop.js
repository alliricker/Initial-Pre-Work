// A common task is to iterate through the content of an array. You can use a for loop - the code will output each element of the array arr to the console.

// Example
var ourArr = [ 9, 10, 11, 12];
var ourTotal = 0;

for (var i = 0; i < ourArr.length; i++) {
  ourTotal += ourArr[i];
}

