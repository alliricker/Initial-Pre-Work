// The push() function is an easy way to append data to the end of an array. Using .push() takes one or more parameters and "pushes" them onto the end of the array.

// Example
var ourArray = ["Stimpson", "J", "cat"];
ourArray.push(["happy", "joy"]);
// ourArray now equals ["Stimpson", "J", "cat", ["happy", "joy"]]

